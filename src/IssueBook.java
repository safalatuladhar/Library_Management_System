        // TODO add your handling code here:
         String bname=jTextField1.getText();
        String idate=jTextField2.getText();
        try{
           Class.forName("com.mysql.jdbc.Driver");
           Connection connection =DriverManager.getConnection("jdbc:mysql://localhost:3306/library","root","");
           String sql="insert into issue_book(bname,idate) values"+"('"+bname+"',"+"'"+idate+"'";
           Statement statment=connection.createStatement();
           int rows= statment.executeUpdate(sql);
           if(rows>0){
               JOptionPane.showMessageDialog(null,"Book returned");
               jTextField1.setText("");
               jTextField2.setText("");
           }
           else{
               JOptionPane.showMessageDialog(null,"book not returned");
           }
           
       }catch(Exception e){
           JOptionPane.showMessageDialog(null, "connection faled");
       }
